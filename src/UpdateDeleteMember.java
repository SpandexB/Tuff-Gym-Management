    private void textFieldAnimation1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_textFieldAnimation1MouseClicked
        // TODO add your handling code here:
        int checkid=0;
        String id=textFieldAnimation1.getText();
        try {
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select *from member where id='"+id+"'");
            while (rs.next()) {
                checkid=1;
                textFieldAnimation1.setEditable(false);
                jTextField2.setText(rs.getString(2));
                jTextField3.setText(rs.getString(3));
                jTextField4.setText(rs.getString(4));
                jTextField5.setText(rs.getString(5));
                jTextField5.setEditable(false);
                jTextField6.setText(rs.getString(6));
                jTextField7.setText(rs.getString(7));
                jTextField7.setEditable(false);
                jTextField8.setText(rs.getString(8));
                jTextField9.setText(rs.getString(9));
                jTextField10.setText(rs.getString(10));
                
                
            }
            if (checkid==0) {
                JOptionPane.showMessageDialog(null,"Member id doesn't exist");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_textFieldAnimation1MouseClicked

